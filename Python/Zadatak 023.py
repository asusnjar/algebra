a = int(input('a = '))
b = int(input('b = '))
operacija = (input('Operacija: '))
if operacija == '+':
    print (f'{a} + {b} = {a+b}')
elif operacija == '-':
    print (f'{a} - {b} = {a+b}')
elif operacija == '*':
    print (f'{a} * {b} = {a+b}')
elif operacija == '/':
    print (f'{a} / {b} = {a+b}')